--[[
用于分号临时英文模式的翻译器
--]]
function semicolon_eng_translator(input, seg)
  local s=input:sub(seg.start, seg._end)
  if ( s:sub(1,1)==";" and s:len()>1) then
    yield(Candidate("eng", seg.start, seg._end, s:sub(2), ""))
  end
end


function single_char_first_filter(input)
  local l = {}
  for cand in input:iter() do
    if (utf8.len(cand.text) == 1) then
      yield(cand)
    else
      table.insert(l, cand)
    end
  end
  for i, cand in ipairs(l) do
    yield(cand)
  end
end


function date_translator(input, seg)
  -- 如果输入串为 `date` 则翻译
  if (input == "date") then
    --[[ 用 `yield` 产生一个候选项
         候选项的构造函数是 `Candidate`，它有五个参数：
          - type: 字符串，表示候选项的类型
          - start: 候选项对应的输入串的起始位置
          - _end:  候选项对应的输入串的结束位置
          - text:  候选项的文本
          - comment: 候选项的注释
     --]]
    yield(Candidate("date", seg.start, seg._end, os.date("%Y年%m月%d日"), "日期"))
    --[[ 用 `yield` 再产生一个候选项
         最终的效果是输入法候选框中出现两个格式不同的当前日期的候选项。
    --]]
    yield(Candidate("date", seg.start, seg._end, os.date("%Y-%m-%d"), "日期"))
  end
end


function time_translator(input, seg)
   if (input == "time") then
      yield(Candidate("time", seg.start, seg._end, os.date("%H:%M:%S"), " 时间"))
   end
end


local charset = {
  ["CJK"] = { first = 0x4E00, last = 0x9FFF },
  ["ExtA"] = { first = 0x3400, last = 0x4DBF },
  ["ExtB"] = { first = 0x20000, last = 0x2A6DF },
  ["ExtC"] = { first = 0x2A700, last = 0x2B73F },
  ["ExtD"] = { first = 0x2B740, last = 0x2B81F },
  ["ExtE"] = { first = 0x2B820, last = 0x2CEAF },
  ["ExtF"] = { first = 0x2CEB0, last = 0x2EBEF },
  ["Compat"] = { first = 0x2F800, last = 0x2FA1F } }


local function is_charset(charset_name, code)
  return (code >= charset[charset_name].first and code <= charset[charset_name].last)
end


local function is_cjk(word)
  for i,c in utf8.codes(word) do
    if (not ( is_charset("CJK",c) or is_charset("ExtA",c) or is_charset("ExtB",c) or is_charset("ExtC",c) or is_charset("ExtD",c) or is_charset("ExtE",c) or is_charset("ExtF",c) or is_charset("Compat",c) )) then
      return false
    end
  end
  return true
end


--[[
  当开启常用字过滤时，只显示gb2312字集中的单字，
  过滤只针对CJK单字，不针对词组，不针对特殊字符
  如果过滤后的CJK单字个数为0，会显示被过滤掉的生僻字
]]--
function charset_filter(input, env)
  -- 获取状态
  local filter_on = env.engine.context:get_option("gb2312")
  -- 已显示的cjk单字个数
  local count = 0
  -- 过滤后的候选项
  local filtered = {}
  -- 过滤前的候选项
  local whole = {}
  if (not filter_on) then
    for cand in input:iter() do
      yield(cand)
    end
  else
    for cand in input:iter() do
      table.insert(whole, cand)
      if (string.len(cand.text) > 3 or (not is_cjk(cand.text) )) then
        table.insert(filtered, cand)
      elseif gb2312_charset[cand.text] then
        table.insert(filtered, cand)
        count = count + 1
      end
    end
    if (count > 0) then
      for i, cand in ipairs(filtered) do
        yield(cand)
      end
    else
      for i, cand in ipairs(whole) do
        yield(cand)
      end
    end
  end
end